$(document).ready(function () {
    $.ajax({
        url: '/Home/Report/monthlyDeposit',
        type: 'GET',
        dataType: 'JSON',
        success: function (json) {

            var amount = []
            var remark = []
            

            let e = eval(json);
            var len = e.length;
            console.log(e[0]);
            
            for (var i = 0; i < len; i++) {
                amount.push(e[i].Total);
                remark.push(e[i].Remark);
            }
            var A = []

            for (var i = 0; i < len; i++) {
                A.push( {
                    name: remark[i],
                    y: amount[i]
                })
            }
            console.log(A);
                /*Highcharts using here.. PIE CHART*/
                Highcharts.chart('container', {
                    chart: {
                        plotBackgroundColor: null,
                        plotBorderWidth: null,
                        plotShadow: false,
                        type: 'pie'
                    },
                    title: {
                        text: 'All deposits of current month'
                    },
                    tooltip: {
                        pointFormat: '{series.name}: <b>{point.y:.1f}$</b>'
                    },
                    accessibility: {
                        point: {
                            valueSuffix: '$'
                        }
                    },
                    plotOptions: {
                        pie: {
                            allowPointSelect: true,
                            cursor: 'pointer',
                            dataLabels: {
                                enabled: true,
                                format: '<b>{point.name}</b>: {point.y:.1f} $'
                            }
                        }
                    },
                    series: [{
                        name: 'Total Amount',
                        colorByPoint: true,
                        data: A
                    }]
                });

        },
        Error: function (json) {
            
        }

        })
    });





